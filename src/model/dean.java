package model;

public class dean {
	private String MADA;
	private String TENDA;
	private String DIADIEMDA;
	private String MAPB;
	public dean() {
	}
	public dean(String mADA, String tENDA, String dIADIEMDA, String mAPB) {
		MADA = mADA;
		TENDA = tENDA;
		DIADIEMDA = dIADIEMDA;
		MAPB = mAPB;
	}
	public String getMADA() {
		return MADA;
	}
	public void setMADA(String mADA) {
		MADA = mADA;
	}
	public String getTENDA() {
		return TENDA;
	}
	public void setTENDA(String tENDA) {
		TENDA = tENDA;
	}
	public String getDIADIEMDA() {
		return DIADIEMDA;
	}
	public void setDIADIEMDA(String dIADIEMDA) {
		DIADIEMDA = dIADIEMDA;
	}
	public String getMAPB() {
		return MAPB;
	}
	public void setMAPB(String mAPB) {
		MAPB = mAPB;
	}
	

}
